#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a[5];
	int		i;
	int		j;

	i = 0;
	a[2] = ' ';
	while (i <= 98)
	{
		j = i + 1;
		a[0] = i / 10 + '0';
		a[1] = i % 10 + '0';
		while (j <= 99)
		{
			a[3] = j / 10 + '0';
			a[4] = j % 10 + '0';
			write(1, a, sizeof(a));
			if (!(i == 98 && j == 99))
				write (1, ", ", 2);
			j++;
		}
		i++;
	}
}
